valorEmMetros = int(input("Digite um valor: "))

valorEmCentimetros = valorEmMetros*100
valorEmMilimetros = valorEmMetros*1000

print(
    f"O valor {valorEmMetros} convertido em centímetros é: {valorEmCentimetros}")
print(
    f"O valor {valorEmMetros} convertido em milímetros é: {valorEmMilimetros}")
